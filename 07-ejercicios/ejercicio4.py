"""
Ejercicio 4
  - Pedir dos números al usuario y hacer todas las
    operaciones básicas de una calculadora y mostrarlo por pantalla
"""

numero1 = int(input("Por favor introducza el primer número: "))
numero2 = int(input("Por favor introducza el segundo número: "))

print("####### CALCULADORA #######")

print(f"{numero1} + {numero2} = {numero1 + numero2}")
print(f"{numero1} - {numero2} = {numero1 - numero2}")
print(f"{numero1} * {numero2} = {numero1 * numero2}")
print(f"{numero1} / {numero2} = {numero1 / numero2}")
